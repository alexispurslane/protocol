func (g *generator) renderNull(b *strings.Builder, imports *fileImports) error {
	imports.add("errors")
	imports.add("github.com/go-json-experiment/json")
	imports.add("github.com/go-json-experiment/json/jsontext")

	lines := docLines("Null represents the JSON null value.", "", "", false)
	for _, line := range commentLines(lines) {
		b.WriteString(line)
		b.WriteByte('\n')
	}
	b.WriteString("type Null struct{}\n\n")

	b.WriteString("func (Null) MarshalJSON() ([]byte, error) {\n")
	b.WriteString("\treturn []byte(\"null\"), nil\n")
	b.WriteString("}\n\n")

	b.WriteString("func (Null) MarshalJSONTo(enc *jsontext.Encoder) error {\n")
	b.WriteString("\treturn enc.WriteToken(jsontext.Null)\n")
	b.WriteString("}\n\n")

	b.WriteString("func (n *Null) UnmarshalJSON(data []byte) error {\n")
	b.WriteString("\tvar value jsontext.Value\n")
	b.WriteString("\tif err := json.Unmarshal(data, &value); err != nil {\n\t\treturn err\n\t}\n")
	b.WriteString("\tif value.Kind() != 'n' {\n")
	b.WriteString("\t\treturn errors.New(\"Null: expected null\")\n\t}\n")
	b.WriteString("\treturn nil\n")
	b.WriteString("}\n\n")

	b.WriteString("func (n *Null) UnmarshalJSONFrom(dec *jsontext.Decoder) error {\n")
	b.WriteString("\tvalue, err := dec.ReadValue()\n")
	b.WriteString("\tif err != nil {\n\t\treturn err\n\t}\n")
	b.WriteString("\treturn n.UnmarshalJSON(value)\n")
	b.WriteString("}\n\n")

	return nil
}

func (g *generator) needsUnionHelpers() bool {
	return len(g.unionDefs) > 0
}

func (g *generator) renderUnionHelpers(imports *fileImports) string {
	imports.add("github.com/go-json-experiment/json")
	imports.add("github.com/go-json-experiment/json/jsontext")

	var b strings.Builder
	b.WriteString("const (\n")
	b.WriteString("\tunionKindNull uint8 = 1 << iota\n")
	b.WriteString("\tunionKindBool\n")
	b.WriteString("\tunionKindString\n")
	b.WriteString("\tunionKindNumber\n")
	b.WriteString("\tunionKindObject\n")
	b.WriteString("\tunionKindArray\n")
	b.WriteString(")\n\n")

	b.WriteString("func unionKindMatches(kind jsontext.Kind, mask uint8) bool {\n")
	b.WriteString("\tswitch kind {\n")
	b.WriteString("\tcase 'n':\n\t\treturn mask&unionKindNull != 0\n")
	b.WriteString("\tcase 't', 'f':\n\t\treturn mask&unionKindBool != 0\n")
	b.WriteString("\tcase '\"':\n\t\treturn mask&unionKindString != 0\n")
	b.WriteString("\tcase '0':\n\t\treturn mask&unionKindNumber != 0\n")
	b.WriteString("\tcase '{':\n\t\treturn mask&unionKindObject != 0\n")
	b.WriteString("\tcase '[':\n\t\treturn mask&unionKindArray != 0\n")
	b.WriteString("\tdefault:\n\t\treturn false\n\t}\n")
	b.WriteString("}\n\n")

	b.WriteString("func decodeObject(value jsontext.Value) (map[string]jsontext.Value, error) {\n")
	b.WriteString("\tvar obj map[string]jsontext.Value\n")
	b.WriteString("\tif err := json.Unmarshal(value, &obj); err != nil {\n\t\treturn nil, err\n\t}\n")
	b.WriteString("\treturn obj, nil\n")
	b.WriteString("}\n\n")

	b.WriteString("func objectFieldEquals(obj map[string]jsontext.Value, name string, want string) bool {\n")
	b.WriteString("\tvalue, ok := obj[name]\n")
	b.WriteString("\tif !ok {\n\t\treturn false\n\t}\n")
	b.WriteString("\tvar got string\n")
	b.WriteString("\tif err := json.Unmarshal(value, &got); err != nil {\n\t\treturn false\n\t}\n")
	b.WriteString("\treturn got == want\n")
	b.WriteString("}\n\n")

	return b.String()
}
