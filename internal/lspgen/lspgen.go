// Type describes a schema type node.
type Type struct {
	Kind    TypeKind `json:"kind"`
	Name    string   `json:"name,omitzero"`
	Items   []*Type  `json:"items,omitzero"`
	Key     *Type    `json:"key,omitzero"`
	Value   *Type    `json:"value,omitzero"`
	Element *Type    `json:"element,omitzero"`

	Literal      *StructureLiteral
	StringValue  *string
	IntegerValue *int
	BoolValue    *bool
}

// rawType is the JSON wire representation of Type.
type rawType struct {
	Kind    TypeKind       `json:"kind"`
	Name    string         `json:"name,omitzero"`
	Items   []*Type        `json:"items,omitzero"`
	Key     *Type          `json:"key,omitzero"`
	Value   jsontext.Value `json:"value,omitzero"`
	Element *Type          `json:"element,omitzero"`
}

// UnmarshalJSON decodes a Type with kind-aware value handling.
func (t *Type) UnmarshalJSON(data []byte) error {
	var raw rawType
	if err := json.Unmarshal(data, &raw); err != nil {
		return err
	}

	t.Kind = raw.Kind
	t.Name = raw.Name
	t.Items = raw.Items
	t.Key = raw.Key
	t.Element = raw.Element

	switch raw.Kind {
	case kindMap:
		if len(raw.Value) > 0 {
			var val Type
			if err := json.Unmarshal(raw.Value, &val); err != nil {
				return err
			}
			t.Value = &val
		}

	case kindLiteral:
		if len(raw.Value) > 0 {
			var lit StructureLiteral
			if err := json.Unmarshal(raw.Value, &lit); err != nil {
				return err
			}
			t.Literal = &lit
		}

	case kindStringLiteral:
		var v string
		if err := json.Unmarshal(raw.Value, &v); err != nil {
			return err
		}
		t.StringValue = &v

	case kindIntegerLiteral:
		var v int
		if err := json.Unmarshal(raw.Value, &v); err != nil {
			return err
		}
		t.IntegerValue = &v

	case kindBooleanLiteral:
		var v bool
		if err := json.Unmarshal(raw.Value, &v); err != nil {
			return err
		}
		t.BoolValue = &v

	default:
		if len(raw.Value) > 0 {
			// ignore
		}
	}

	return nil
}

