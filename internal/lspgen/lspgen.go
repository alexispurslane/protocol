// goPublicIdentifier converts a schema identifier to a Go-exported name.
func goPublicIdentifier(name string) string {
	if name == "" {
		return ""
	}

	words := splitIdentifierWords(name)
	if len(words) == 0 {
		return ""
	}

	var b strings.Builder
	for word := range slices.Values(words) {
		if word == "" {
			continue
		}

		upper := strings.ToUpper(word)
		if _, ok := commonInitialisms[upper]; ok {
			b.WriteString(upper)
			continue
		}

		// Handle plural initialisms: "Ids" -> "IDs", "Uris" -> "URIs".
		if strings.HasSuffix(word, "s") && len(word) > 1 {
			base := word[:len(word)-1]
			baseUpper := strings.ToUpper(base)
			if _, ok := commonInitialisms[baseUpper]; ok {
				b.WriteString(baseUpper)
				b.WriteByte('s')
				continue
			}
			// Preserve pluralized acronyms not in the initialism list: "ABAPs" -> "ABAPs".
			if baseUpper == base && len(base) > 1 {
				b.WriteString(base)
				b.WriteByte('s')
				continue
			}
		}

		// Preserve already-uppercase words (e.g. ABAP, CPP) that aren't in the
		// initialism list.
		if upper == word {
			b.WriteString(word)
			continue
		}

		lower := strings.ToLower(word)
		b.WriteString(strings.ToUpper(lower[:1]))
		b.WriteString(lower[1:])
	}

	out := b.String()
	if out == "" {
		return ""
	}

	// Avoid invalid identifiers if we ever encounter a name starting with a digit.
	if isDigit(out[0]) {
		return "X" + out
	}

	return out
}

// splitIdentifierWords splits an identifier into word-like chunks.
func splitIdentifierWords(s string) []string {
	if s == "" {
		return nil
	}

	var words []string
	start := 0

	flush := func(i int) {
		if start < i {
			words = append(words, s[start:i])
		}
	}

	// Split on non-alnum boundaries and on case/digit transitions within words.
	for i := range len(s) {
		ch := s[i]
		if !isAlphaNum(ch) {
			flush(i)
			start = i + 1
			continue
		}
		if i == start {
			continue
		}

		prev := s[i-1]
		if !isAlphaNum(prev) {
			continue
		}

		if isUpper(ch) && isDigit(prev) {
			flush(i)
			start = i
			continue
		}
		if isUpper(ch) && isLower(prev) {
			flush(i)
			start = i
			continue
		}
		if isUpper(ch) && isUpper(prev) && i+1 < len(s) && isLower(s[i+1]) {
			// Split "JSONText" into ["JSON", "Text"].
			flush(i)
			start = i
			continue
		}
	}

	flush(len(s))

	return words
}

// isUpper reports whether b is an ASCII uppercase letter.
func isUpper(b byte) bool { return b >= 'A' && b <= 'Z' }

// isLower reports whether b is an ASCII lowercase letter.
func isLower(b byte) bool { return b >= 'a' && b <= 'z' }

// isLetter reports whether b is an ASCII letter.
func isLetter(b byte) bool { return isUpper(b) || isLower(b) }

// isDigit reports whether b is an ASCII digit.
func isDigit(b byte) bool { return b >= '0' && b <= '9' }

// isAlphaNum reports whether b is an ASCII letter or digit.
func isAlphaNum(b byte) bool { return isLetter(b) || isDigit(b) }

// resolveType converts a schema Type into its GoType representation.
func resolveType(t *Type) GoType {
	switch t.Kind {
	case kindBase:
		switch t.Name {
		case "integer":
			return GoType{
				Name:         "int32",
				NeedsPointer: false,
			}

		case "uinteger":
			return GoType{
				Name:         "uint32",
				NeedsPointer: false,
			}

		case "string":
			return GoType{
				Name:         "string",
				NeedsPointer: false,
			}

		case "boolean":
			return GoType{
				Name:         "bool",
				NeedsPointer: false,
			}

		case "URI":
			return GoType{
				Name:         "*uri.URI",
				NeedsPointer: false,
			}

		case "DocumentUri":
			return GoType{
				Name:         "*uri.URI",
				NeedsPointer: false,
			}

		case "decimal":
			return GoType{
				Name:         "float64",
				NeedsPointer: false,
			}

		case "null":
			return GoType{
				Name:         "any",
				NeedsPointer: false,
			}

		default:
			panic(fmt.Sprintf("Unsupported base type: %s", t.Name))
		}

	case kindReference:
		// Resolve aliases/overrides first; unknown references are recorded for later output.
		if override, ok := typeAliasOverrides[t.Name]; ok {
			return override
		}
		if aliased, ok := typeInfo.TypeAliasMap[t.Name]; ok {
			return resolveType(aliased)
		}
		if refType, ok := typeInfo.Types[t.Name]; ok {
			return refType
		}
		refType := GoType{
			Name:         goPublicIdentifier(t.Name),
			NeedsPointer: true,
		}
		typeInfo.Types[t.Name] = refType

		return refType

	case kindArray:
		// Arrays themselves are values; element pointer-ness is encoded in the element type.
		elem := resolveType(t.Element)
		name := "[]" + elem.Name
		if elem.NeedsPointer {
			name = "[]*" + elem.Name
		}

		return GoType{
			Name:         name,
			NeedsPointer: false,
		}

	case kindMap:
		// Map values preserve pointer-ness to keep optional/nullable semantics.
		key := resolveType(t.Key)
		val := resolveType(t.Value)
		valName := val.Name
		if val.NeedsPointer {
			valName = "*" + valName
		}

		return GoType{
			Name:         fmt.Sprintf("map[%s]%s", key.Name, valName),
			NeedsPointer: false,
		}

	case kindTuple:
		// The only supported tuple is [uinteger, uinteger], which maps to a fixed array.
		if len(t.Items) == 2 && t.Items[0].Kind == kindBase && t.Items[0].Name == "uinteger" && t.Items[1].Kind == kindBase && t.Items[1].Name == "uinteger" {
			return GoType{
				Name:         "[2]uint32",
				NeedsPointer: false,
			}
		}
		panic(fmt.Sprintf("Unsupported tuple type: %v", t))

	case kindStringLiteral:
		typeName := "StringLiteral" + titleCase(*t.StringValue)
		typeInfo.LiteralTypes.Set(fmt.Sprintf("%v", *t.StringValue), typeName)

		return GoType{
			Name:         typeName,
			NeedsPointer: false,
		}

	case kindIntegerLiteral:
		typeName := fmt.Sprintf("IntegerLiteral%d", *t.IntegerValue)
		typeInfo.LiteralTypes.Set(fmt.Sprintf("%v", *t.IntegerValue), typeName)

		return GoType{
			Name:         typeName,
			NeedsPointer: false,
		}

	case kindBooleanLiteral:
		typeName := "BooleanLiteral"
		if *t.BoolValue {
			typeName += "True"
		} else {
			typeName += "False"
		}
		typeInfo.LiteralTypes.Set(fmt.Sprintf("%v", *t.BoolValue), typeName)

		return GoType{
			Name:         typeName,
			NeedsPointer: false,
		}

	case kindLiteral:
		// Only empty object literals are supported; others are schema errors.
		if t.Literal != nil && len(t.Literal.Properties) == 0 {
			return GoType{
				Name:         "struct{}",
				NeedsPointer: false,
			}
		}
		panic(fmt.Sprintf("Unexpected non-empty literal object: %v", t.Literal))

	case kindOr:
		// Unions become wrapper types with custom (un)marshal logic.
		return handleOrType(t)

	default:
		panic(fmt.Sprintf("Unsupported type kind: %s", t.Kind))
	}
}

// flattenOrTypes expands nested union types and removes duplicates.
func flattenOrTypes(types []*Type) []*Type {
	flattened := []*Type{}
	// seen tracks raw node pointers so we dedupe while preserving first-seen order.
	seen := make(map[*Type]struct{})

	add := func(raw *Type) {
		if raw == nil {
			return
		}

		t := raw
		if raw.Kind == kindReference {
			// Inline union aliases so nested "or" types don't become nested wrappers.
			if aliased, ok := typeInfo.TypeAliasMap[raw.Name]; ok && aliased.Kind == kindOr {
				t = aliased
			}
		}

		if t.Kind == kindOr {
			// Recursively inline nested unions but keep the outer ordering stable.
			for sub := range slices.Values(flattenOrTypes(t.Items)) {
				if _, ok := seen[sub]; !ok {
					seen[sub] = struct{}{}
					flattened = append(flattened, sub)
				}
			}
			return
		}

		if _, ok := seen[raw]; !ok {
			seen[raw] = struct{}{}
			flattened = append(flattened, raw)
		}
	}

	for t := range slices.Values(types) {
		add(t)
	}

	return flattened
}

// pluralize applies minimal English pluralization for identifier parts.
func pluralize(name string) string {
	if strings.HasSuffix(name, "s") || strings.HasSuffix(name, "x") || strings.HasSuffix(name, "z") || strings.HasSuffix(name, "ch") || strings.HasSuffix(name, "sh") {
		return name + "es"
	}

	if strings.HasSuffix(name, "y") && len(name) > 1 {
		penultimate := name[len(name)-2 : len(name)-1]
		if !strings.ContainsAny(penultimate, "aeiou") {
			return name[:len(name)-1] + "ies"
		}
	}

	return name + "s"
}

// handleOrType resolves a union type into a generated Go union wrapper.
func handleOrType(orType *Type) GoType {
	// Flatten nested unions first so naming and member ordering are deterministic.
	types := flattenOrTypes(orType.Items)

	// Extract a single null branch to drive optional union handling.
	nullIndex := -1
	for i, t := range types {
		if t.Kind == kindBase && t.Name == "null" {
			nullIndex = i
			break
		}
	}
	containedNull := nullIndex != -1

	nonNullTypes := types
	if containedNull {
		// Preserve ordering while removing the single null branch.
		nonNullTypes = append([]*Type{}, types[:nullIndex]...)
		nonNullTypes = append(nonNullTypes, types[nullIndex+1:]...)
	}
	if len(nonNullTypes) == 0 {
		panic(fmt.Sprintf("Union type with only null is not supported: %v", types))
	}

	// Derive member field names from type shapes to keep union fields meaningful.
	memberNames := make([]string, len(nonNullTypes))
	for i, t := range nonNullTypes {
		switch t.Kind {
		case kindReference:
			memberNames[i] = goPublicIdentifier(t.Name)

		case kindBase:
			memberNames[i] = goPublicIdentifier(t.Name)

		case kindArray:
			if t.Element.Kind == kindReference || t.Element.Kind == kindBase {
				memberNames[i] = pluralize(goPublicIdentifier(t.Element.Name))
			} else {
				elem := resolveType(t.Element)
				memberNames[i] = elem.Name + "Array"
			}

		case kindLiteral:
			if t.Literal != nil && len(t.Literal.Properties) == 0 {
				memberNames[i] = "EmptyObject"
			} else {
				panic(fmt.Sprintf("Unsupported type kind in union: %s", t.Kind))
			}

		case kindTuple:
			memberNames[i] = "Tuple"

		default:
			panic(fmt.Sprintf("Unsupported type kind in union: %s", t.Kind))
		}
	}

	findLongestCommonPrefix := func(names []string) string {
		if len(names) <= 1 {
			return ""
		}

		splitPascalCase := func(name string) []string {
			var chunks []string
			current := ""
			for i := range len(name) {
				ch := name[i]
				// Start a new chunk when we hit an uppercase boundary.
				if isUpper(ch) && current != "" {
					chunks = append(chunks, current)
					current = string(ch)
				} else {
					current += string(ch)
				}
			}
			if current != "" {
				chunks = append(chunks, current)
			}
			return chunks
		}

		allChunks := make([][]string, len(names))
		minLen := -1
		for i, n := range names {
			chunks := splitPascalCase(n)
			allChunks[i] = chunks
			if minLen == -1 || len(chunks) < minLen {
				minLen = len(chunks)
			}
		}

		var common []string
		for i := range minLen {
			chunk := allChunks[0][i]
			allMatch := true
			for c := range slices.Values(allChunks[1:]) {
				if c[i] != chunk {
					allMatch = false
					break
				}
			}
			if !allMatch {
				break
			}
			common = append(common, chunk)
		}

		return strings.Join(common, "")
	}

	// Prefer a shared prefix to keep union type names compact.
	commonPrefix := findLongestCommonPrefix(memberNames)
	unionTypeName := ""

	if commonPrefix != "" {
		trimmed := make([]string, len(memberNames))
		allNonEmpty := true
		for i, name := range memberNames {
			trimmed[i] = strings.TrimPrefix(name, commonPrefix)
			if trimmed[i] == "" {
				allNonEmpty = false
			}
		}
		if allNonEmpty {
			unionTypeName = commonPrefix + strings.Join(trimmed, "Or")
			memberNames = trimmed
		} else {
			unionTypeName = strings.Join(memberNames, "Or")
		}
	} else {
		unionTypeName = strings.Join(memberNames, "Or")
	}

	// Special-case RegisterOptions unions to keep stable naming across versions.
	if orType == registerOptionsUnionType {
		// Collapse "XRegistrationOptions" members to "X" for stable union members.
		unionTypeName = "RegisterOptions"
		for i, name := range memberNames {
			if before, ok := strings.CutSuffix(name, "RegistrationOptions"); ok {
				memberNames[i] = before
			}
		}
	}

	if containedNull {
		// Only union types that explicitly included null get the OrNull suffix.
		unionTypeName += "OrNull"
	} else {
		containedNull = false
	}

	union := make([]UnionMember, len(memberNames))
	for i, name := range memberNames {
		union[i] = UnionMember{
			Name:          name,
			Type:          nonNullTypes[i],
			ContainedNull: containedNull,
		}
	}

	// Register the union for later code generation.
	typeInfo.UnionTypes.Set(unionTypeName, union)

	return GoType{
		Name:         unionTypeName,
		NeedsPointer: false,
	}
}

